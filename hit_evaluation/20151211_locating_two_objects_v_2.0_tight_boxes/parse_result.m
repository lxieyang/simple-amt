function [assignment_id, ...
            image_url, ...
            coord_first, ...
            coord_second, ...
            worker_id, ...
            hit_id] = parse_result(file_name)
    % read in each line
    lines = read_in_file_lines(file_name);
    
    % parse each line
    for i = 1 : numel(lines)
        [assignment_id{i}, ...
         image_url{i}, ...
         coord_first{i}, ...
         coord_second{i}, ...
         worker_id{i}, ...
         hit_id{i}] = parse_single_line(lines{i});
    end
    
end